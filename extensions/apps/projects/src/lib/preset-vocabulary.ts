/** 创建词汇项 */
function createVocabItems(category: string, words: string[]): ResourceItem[] {
  const now = Date.now();
  return words.map((word, index) => ({
    id: `vocab-${category}-${now}-${index}`,
    content: word,
    type: 'vocabulary' as const,
    category,
    categoryName: category,
    tags: [category],
    createdAt: now,
  }));
}

/** 获取词汇统计 */
export function getVocabularyStats() {
  const stats: Record<string, number> = {};
  PRESET_VOCABULARY.forEach(item => {
    stats[item.category] = (stats[item.category] || 0) + 1;
  });
  return stats;
}
